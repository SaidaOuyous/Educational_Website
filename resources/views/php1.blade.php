    <h1 class="text-2xl font-bold">Qu'est-ce que PHP ?</h1>
    <p> &nbsp;&nbsp; <span style="color:#eec60a ;font-size:23px" class="mb-7"> PHP  </span> 
    <span style="color:#eec60a ;font-size:23px"> PHP  </span>  (Hypertext Preprocessor) est un langage de script côté serveur utilisé pour développer des applications web dynamiques. Il s'exécute côté serveur avant que le contenu ne soit envoyé au navigateur, ce qui permet de générer du contenu dynamique basé sur les données provenant d'une base de données, de fichiers, ou d'autres sources.  <span style="color:#eec60a ;font-size:23px"> PHP  </span>  est souvent utilisé en conjonction avec des bases de données pour créer des sites web interactifs et évolutifs.


